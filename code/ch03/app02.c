/* platinum.c — ваш вес в платиновом эквиваленте (ввод в килограммах) */
#include <stdio.h>

int main(void)
{
    float weight_kg;   /* вес пользователя в килограммах */
    float weight_lb;   /* вес в фунтах */
    float value;       /* платиновый эквивалент */

    printf("Хотите узнать свой вес в платиновом эквиваленте?\n");
    printf("Давайте подсчитаем.\n");
    printf("Пожалуйста, введите свой вес в килограммах: ");

    /* получить входные данные от пользователя */
    scanf("%f", &weight_kg);

    /* переводим килограммы в фунты (1 кг = 2.20462 фунта) */
    weight_lb = weight_kg * 2.20462f;

    /* цена платины: $1700 за тройскую унцию */
    /* 14.5833 — коэффициент перевода фунтов в тройские унции */
    value = 1700.0f * weight_lb * 14.5833f;

    printf("\nВаш вес: %.2f кг (%.2f фунтов)\n", weight_kg, weight_lb);
    printf("Ваш вес в платиновом эквиваленте составляет $%.2f.\n", value);
    printf("Вы легко можете стать достойным этого!\n");
    printf("Если цена платины падает — ешьте больше для поддержания своей стоимости.\n");

    return 0;
}
